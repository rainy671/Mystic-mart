    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        if(!validateInputs())
        return;
          try{
              String userid=cbReceptionistId.getSelectedItem().toString();
              boolean result=ReceptionistDAO.updatePassword(userid,pwd);
              if(result)
              {
                  JOptionPane.showMessageDialog(null,"Password Updated successfully!");
                  clearText();
                  cbReceptionistId.setSelectedIndex(0);

                  return;
              }
              JOptionPane.showMessageDialog(null,"Password not Updated!");
              clearText();
              
          }
          catch(SQLException ex){
               JOptionPane.showMessageDialog(null, "DB Error!","Error",JOptionPane.ERROR_MESSAGE);
                 ex.printStackTrace();
          }
    }//GEN-LAST:event_btnUpdateActionPerformed

