private void loadReceptionistDetails(){
        try
    {
        receptionist=ReceptionistDAO.getAllReceptionistId();
        if(receptionist.isEmpty())
        {
             JOptionPane.showMessageDialog(null, "No receptionist present","Error",JOptionPane.ERROR_MESSAGE);
             btnUpdate.setEnabled(false);
             return;
        }
        btnUpdate.setEnabled(true);
        Set<String> keys=receptionist.keySet();
       
        for(String id:keys)
        {
          cbReceptionistId.addItem(id);
        }
    }
        catch(SQLException ex){
         JOptionPane.showMessageDialog(null, "DB Error!","Error",JOptionPane.ERROR_MESSAGE);
         ex.printStackTrace();
    }
    
}
private boolean validateInputs(){
     if(pfPassword.getPassword().length<4||pfRePassword.getPassword().length<4){
          JOptionPane.showMessageDialog(null, "Password must be atleast 4 characters long","Error!",JOptionPane.ERROR_MESSAGE);
            return false;}
      pwd=String.valueOf(pfPassword.getPassword());
     String repwd=String.valueOf(pfRePassword.getPassword());
     if(pwd.equals(repwd)==false)
     {
         JOptionPane.showMessageDialog(null, "Passwords do not match!","Error",JOptionPane.ERROR_MESSAGE);
         return false;
     }
     return true;
}
private void clearText(){
    pfPassword.setText("");
     pfPassword.setText("");
     
}

