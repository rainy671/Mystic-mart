    private void loadEmpId() {
         try{
    List <String> idList=  EmployeesDAO.getAllEmpid();
    if(idList.isEmpty())
    {
        JOptionPane.showMessageDialog(null,"No records present!","Error!",JOptionPane.ERROR_MESSAGE);
        return;
    }
    btnRemove.setEnabled(true);
    for(String empid:idList)
    {
       cbEmpId.addItem(empid);
    }
   
    
        }
     
     catch(SQLException ex)
     {
         JOptionPane.showMessageDialog(null,"Problem in database!","Database error!",JOptionPane.ERROR_MESSAGE);
         ex.printStackTrace();
     }
    }
     private void clearText() {
       tfEmpName.setText("");
       tfSalary.setText("");
       cbEmpId.setSelectedIndex(0);
       cbJob.setText("");
    }
