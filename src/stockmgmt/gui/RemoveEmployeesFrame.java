    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
       try{
        boolean result=EmployeesDAO.removeEmployee(cbEmpId.getSelectedItem().toString());
       if(result)
       {
           JOptionPane.showMessageDialog(null, "Employee Removed!");
           
           cbEmpId.removeItemAt(cbEmpId.getSelectedIndex());
           clearText();
           return;
           
       }
       JOptionPane.showMessageDialog(null, "Could not delete the record");
       }
       catch(SQLException ex)
       {
       JOptionPane.showMessageDialog(null,"Problem in database!","Database error!",JOptionPane.ERROR_MESSAGE);
         ex.printStackTrace();
       }
    }//GEN-LAST:event_btnRemoveActionPerformed

    private void cbEmpIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbEmpIdActionPerformed
         try
        { String empid=cbEmpId.getSelectedItem().toString();
        EmployeesPojo e=EmployeesDAO.findEmployeeById(empid);
        tfEmpName.setText(e.getEmpname());
         cbJob.setText(e.getJob());
        tfSalary.setText(String.valueOf(e.getSalary()));
       
        }
        catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null,"Error in DB!","Error!",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_cbEmpIdActionPerformed

