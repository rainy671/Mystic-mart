  private void loadProductDetails() {
         try{
            List<ProductsPojo> al=ProductsDAO.getProductDetails();
            if(al.isEmpty())
            {
                JOptionPane.showMessageDialog(null, "No Product Present", "Error!", JOptionPane.ERROR_MESSAGE);
                return;
            }
            btnUpdate.setEnabled(true);
            Object []rows=new Object[7];
            tm=(DefaultTableModel)tblProducts.getModel();
            for (ProductsPojo p:al)
            {
                rows[0]=p.getProductId();
                rows[1]=p.getProductName();
                 rows[2]=p.getProductCompany();
                 rows[3]=p.getProductPrice();
                 rows[4]=p.getOurPrice();
                 rows[5]=p.getTax()+"%";
                 rows[6]=p.getQuantity();
                 tm.addRow(rows);
            }
            
        }
        catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null, "Problem in loading product details. DB Error", "Error!", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
   }

    private boolean validateInputs() {
        return !(txtpid.getText().isEmpty()||txtPname.getText().isEmpty()||txtPcompany.getText().isEmpty()||txtPprice.getText().isEmpty()||txtOurprice.getText().isEmpty()||txtquantity.getText().isEmpty());
    }
