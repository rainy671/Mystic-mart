    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        if(tblProducts.getSelectedRow()==-1)
        {
            JOptionPane.showMessageDialog(null, "Please select a row","Error!",JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(!validateInputs())
        {
            JOptionPane.showMessageDialog(null, "Please fill all the fields","Error!",JOptionPane.ERROR_MESSAGE);
            return;
        }
        try{
            
        ProductsPojo p=new ProductsPojo();
        p.setProductId(txtpid.getText());
        p.setProductName(txtPname.getText());
        p.setProductCompany(txtPcompany.getText());
        p.setProductPrice(Double.parseDouble(txtPprice.getText()));
        p.setOurPrice(Double.parseDouble(txtOurprice.getText()));
        p.setQuantity(Integer.parseInt(txtquantity.getText()));
        int n= cbTax.getSelectedItem().toString().length();
        p.setTax(Integer.parseInt(cbTax.getSelectedItem().toString().substring(0,n-1)));
        boolean result=ProductsDAO.updateProduct(p);
        if(result)
        {
            JOptionPane.showMessageDialog(null, "Product Updated Successfully");
            tm.setRowCount(0);
            loadProductDetails();
            btnUpdate.setEnabled(false);
            return;
        }
         JOptionPane.showMessageDialog(null, "Product Updation Failed");
        }
        catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null, "DB Error", "Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
        catch(NumberFormatException ex)
        {
           JOptionPane.showMessageDialog(null, "Please input digits only!"); 
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

