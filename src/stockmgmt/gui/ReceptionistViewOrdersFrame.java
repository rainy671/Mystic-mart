private void loadOrderIDs()
{
   try{
      
       List<String> oid=OrdersDAO.getReceptionistOrderIDs();
       if(oid.isEmpty())
       {
           JOptionPane.showMessageDialog(null, "No orders present!", "Error", JOptionPane.ERROR_MESSAGE);
           return;
       }
       btnShow.setEnabled(true);
       for(String orderID:oid)
       {
       cbOrderId.addItem(orderID);
       }
   }
    catch(SQLException ex)
     {
         JOptionPane.showMessageDialog(null,"Problem in database!","Database error!",JOptionPane.ERROR_MESSAGE);
         ex.printStackTrace();
     }
}

