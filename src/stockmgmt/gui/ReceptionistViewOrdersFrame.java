    private void btnShowActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnShowActionPerformed
        String orderid=cbOrderId.getSelectedItem().toString();
        tm=(DefaultTableModel)jtView.getModel();
        if(tm.getRowCount()!=0)
            tm.setRowCount(0);
        try{
            ArrayList<ProductsPojo> pl=OrdersDAO.getOrderDetails(orderid);
            Object[] cols=new Object[7];
            for(ProductsPojo p:pl)
            {
                cols[0]=p.getProductId();
                cols[1]=p.getProductName();
                cols[2]=p.getProductCompany();
                cols[3]=p.getProductPrice();
                cols[4]=p.getOurPrice();
                cols[5]=p.getQuantity();
                cols[6]=p.getTax()+"%";
                tm.addRow(cols);
            }
            
        }
        
        catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null,"Problem in database!","Database error!",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnShowActionPerformed

