    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
    int result=validateInputs();
    if(result==0){
        JOptionPane.showMessageDialog(null, "Password must be atleast 4 characters long","Error!",JOptionPane.ERROR_MESSAGE);
    return;}
    if(result==-1){
        JOptionPane.showMessageDialog(null, "ReceptionistID cannot be empty!","Error",JOptionPane.ERROR_MESSAGE);
        return;
    }
    char[] pwd=pfPassword.getPassword();
    char[] repwd=pfRePassword.getPassword();
    String strpwd= String.valueOf(pwd);
    String strrepwd= String.valueOf(repwd);
    if(isPasswordMatching(strpwd,strrepwd)==false)
    {
         JOptionPane.showMessageDialog(null, "Passwords do not match!","Error",JOptionPane.ERROR_MESSAGE);
        return;
    }
    try{
        String empid=cbEmpId.getSelectedItem().toString();
        String empname=txtEmpName.getText();
         String userid=txtUserId.getText();
         UsersPojo user=new UsersPojo();
         user.setEmpid(empid);
         user.setUsername(empname);
         user.setUserid(userid);
         user.setPassword(strpwd);
         user.setUsertype("Receptionist");
         boolean status=ReceptionistDAO.addReceptionist(user);
         if(status){
             JOptionPane.showMessageDialog(null, "Receptionist added successfully");
             clearText();
             loadReceptionistDetails();
             return;
         }}
         catch(SQLException ex){
                 JOptionPane.showMessageDialog(null, "DB Error!","Error",JOptionPane.ERROR_MESSAGE);
                 ex.printStackTrace();
    }
    }//GEN-LAST:event_btnAddActionPerformed

