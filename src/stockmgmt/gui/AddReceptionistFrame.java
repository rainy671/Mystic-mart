private void loadReceptionistDetails()
{
    try
    {
        receptionist=ReceptionistDAO.getNonRegisteredReceptionists();
        if(receptionist.isEmpty())
        {
             JOptionPane.showMessageDialog(null, "No unregistered receptionist present","Error",JOptionPane.ERROR_MESSAGE);
             btnAdd.setEnabled(false);
             return;
        }
        btnAdd.setEnabled(true);
        Set<String> keys=receptionist.keySet();
        Iterator<String> it=keys.iterator();
        cbEmpId.removeAllItems();
        while(it.hasNext())
        {
            cbEmpId.addItem(it.next());
        }
    }
        catch(SQLException ex){
                 JOptionPane.showMessageDialog(null, "DB Error!","Error",JOptionPane.ERROR_MESSAGE);
                 ex.printStackTrace();
    }
    
}
